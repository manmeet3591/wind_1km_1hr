def patchify_4d(data, patch_size):
    """
    Vectorized patchify for 4D data (T, C, H, W).
    Returns: (T, C, N_patches, patch_size, patch_size)
    """
    T, C, H, W = data.shape
    assert H % patch_size == 0 and W % patch_size == 0, "H and W must be divisible by patch_size"

    H_patches = H // patch_size
    W_patches = W // patch_size

    patches = data.reshape(T, C, H_patches, patch_size, W_patches, patch_size)
    patches = patches.transpose(0, 1, 2, 4, 3, 5)  # (T, C, H_p, W_p, ph, pw)
    patches = patches.reshape(T, C, H_patches * W_patches, patch_size, patch_size)
    return patches

def unpatchify_4d(patches, original_shape):
    """
    Vectorized unpatchify for 4D data.
    Input: (T, C, N_patches, patch_size, patch_size)
    Output: (T, C, H, W)
    """
    T, C, N, patch_size, _ = patches.shape
    H, W = original_shape[2], original_shape[3]
    H_patches = H // patch_size
    W_patches = W // patch_size

    assert N == H_patches * W_patches, "Number of patches does not match expected count"

    data = patches.reshape(T, C, H_patches, W_patches, patch_size, patch_size)
    data = data.transpose(0, 1, 2, 4, 3, 5)  # (T, C, H_p, ph, W_p, pw)
    data = data.reshape(T, C, H, W)
    return data

